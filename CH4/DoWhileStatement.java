public class DoWhileStatement {
    public static void main(String[] args) {
        boolean condition = false;
        // do - while 시작
        do {
            System.out.println("Do는 조건문이 어찌되든 일단 실행");
        } while(condition);
        // do-while 종료
        System.out.println("Do-While 종료");
    }
}
